function wrapPriceCode(title) {
    // –ò—â–µ–º –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏—é –≤–∏–¥–∞: p(–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ) + 3 —Ü–∏—Ñ—Ä—ã + üí∞(–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ)
    // –ú–µ–∂–¥—É —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏ –º–æ–≥—É—Ç –±—ã—Ç—å –ø—Ä–æ–±–µ–ª—ã
    const regex = /(?:p\s*)?(\d{3})(?:\s*üí∞)?/g;
    
    // –ó–∞–º–µ–Ω—è–µ–º –Ω–∞–π–¥–µ–Ω–Ω—ã–µ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏, –æ–±–æ—Ä–∞—á–∏–≤–∞—è –∏—Ö –≤ –¥–≤–æ–π–Ω—ã–µ —Å–∫–æ–±–∫–∏
    return title.replace(regex, '(( p$1 üí∞ ))');
}

